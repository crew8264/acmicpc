#include <stdio.h>

int main() {
    int A, B;
    int ans;
    
    scanf("%d %d", &A, &B);
    
    ans = A+B;
    printf("%d\n", ans);
    
    ans = A-B;
    printf("%d\n", ans);
    
    ans = A*B;
    printf("%d\n", ans);
    
    ans = A/B;
    printf("%d\n", ans);
    
    ans = A%B;
    printf("%d\n", ans);
    
}
